public class Hero {
    int salud = 100;
    int energia = 100;

    public Hero(int salud, int energia) {
        this.salud = salud;
        this.energia = energia;
    }

    public int getSalud() {
        return salud;
    }

    public void setSalud(int salud) {
        this.salud = salud;
    }

    public int getEnergia() {
        return energia;
    }

    public void setEnergia(int energia) {
        this.energia = energia;
    }




}


